public class BasicOperations {
    
    //adition, substraction, division, multiplication 

    public static float addTwoNumbers(float operand1, float operand2){
           float sum = 0;
           sum = operand1 + operand2;
           return sum;
}
 public static float susbtracTwoNumbers(float operand1, float operand2){
           float subtraction = 0;
           subtraction = operand1 - operand2;
           return subtraction;
}
public static float multiplyTwoNumbers(float operand1, float operand2){
           float multiplication = 0;
           multiplication = operand1 * operand2;
           return multiplication;
}
 public static float divideTwoNumbers(float operand1, float operand2){
           float division = 0;
           division = operand1 / operand2;
           return division;
 }
}
        